            <h3>Rádio Web UFPA lança webdocumentário acessível em homenagem aos 60 anos da Universidade</h3>

            <p><i>Quantas memórias podem ser acumuladas em seis décadas? Quais os desafios para o futuro da Universidade? O webdocumentário “UFPA 60 anos”, produzido pela Rádio Web UFPA, registra um valioso acervo multimídia sobre a trajetória, os desafios e as perspectivas da instituição. Todo o conteúdo estará acessível às pessoas com deficiência.</i></p>

            <img src="tourinho.jpg">

            <p>Para homenagear as seis décadas de fundação da maior instituição de ensino, pesquisa e extensão da região Norte do País, uma história que começou oficialmente no dia 02 de julho de 1957, a Rádio Web UFPA produziu um webdocumentário experimental, que agrega convergência midiática e acessibilidade, para retratar um pouco da construção e das contribuições da Universidade Federal do Pará (UFPA) para o desenvolvimento da Amazônia paraense.</p>

            <p>Durante três meses, jornalistas e bolsistas do projeto se dedicaram intensamente à elaboração e execução do webdocumentário, uma formato inovador, multimidiático que de forma convergente agrega textos, vídeos, fotos e áudios, armazenados, sincronizados e disponíveis na internet, para que o usuário possa acessar o conteúdo e interagir com ele. Ao longo desse tempo foram realizadas mais de 70 entrevistas e depoimentos com alunos, professores e técnico-administrativos para ajudar a contar a história da UFPA.</p>

            <p>Além do formato, a produção do Webdoc trouxe para a equipe da rádio um importante desafio, que é de toda instituição pública: a proposta de garantir uma comunicação inclusiva. Todos os conteúdos do webdocumentário, independente do formado da mídia, estarão acessíveis para pessoas com deficiência.</p>

            <p>O produto estará disponível a partir do dia 22 de agosto, às 11h, em: <a href="../">radio.ufpa.br/webdoc60</a>, quando o site especial do Webdoc será lançado em evento no hall do Espaço de Ensino Mirante do Rio.</p>

            <h4>Conteúdo</h4>

            <p>O Webdocumentário UFPA 60 anos tem seis seções em que o usuário vai poder navegar de diferentes formas pelas memórias acumuladas desde a implantação da UFPA, no ano de 1957, até os dias atuais com todos os desafios e expectativas da comunidade acadêmica.</p>

            <p>Aproveitando a experiência da Rádio Web UFPA na área de radiojornalismo científico, um dos conteúdos disponíveis é o <a href="#">“Radiodocumentário UFPA 60 Anos”</a>. O material destaca a história e os avanços do ensino, da pesquisa e da extensão na Amazônia em seis capítulos com os seguintes temas: “Dos anos dourados aos anos de chumbo, a construção da Universidade na Amazônia”; “Ensino para a inclusão e cidadania na Amazônia”; “A força da pesquisa na Amazônia”; “O diálogo entre a Universidade e a Sociedade na Amazônia”; “A integração dos saberes no interior da Amazônia” e, ainda, “Sonhos para transformar a realidade na Amazônia”.</p>

            <p>Em outra seção denominada <a href="#">“Memórias UFPA”</a> são utilizados infográficos para destacar fatos relevantes na linha do tempo e personalidades que prestaram serviços importantes para a instituição e para a sociedade, por exemplo, reitores, professores eméritos e doutores honoris causa. Além do conteúdo em textos e fotos, nesta seção, o internauta pode conferir entrevistas, em vídeo, com o atual reitor Emmanuel Tourinho. E, ainda, com a professora emérita Violeta Refkalefsky Loureiro, doutora em sociologia e especialista em temas como Amazônia, desenvolvimento e conflitos. E também com o doutor honoris causa Dom Luís Azcona, Bispo Emérito da Prelazia do Marajó e um grande defensor dos direitos humanos na região.</p>

            <img src="violeta.jpg">

            <p>Já a seção <a href="#">“UFPA de Todos”</a> traz entrevistas e depoimentos, também em vídeos, de quem fez a ainda faz parte da Universidade. São relatos de professores, estudantes e técnico-administrativos que, por meio de suas histórias e o contato direto com a instituição, destacam a importância das conquistas em 60 anos de produção de conhecimento, sem esquecer os problemas e os desafios em busca de uma universidade pública de maior excelência científica e cada vez mais inclusiva e cidadã.</p>

            <p>Há, ainda, a seção <a href="#">“Sons da UFPA”</a>, que reúne os áudios característicos do cotidiano da Universidade e do campus do Guamá, como o motor das embarcações que passam diariamente próximo à beira do rio e o latido dos cachorros que habitam e fazem parte da rotina do campus. Além disso, o site do Webdoc será atualizado com reportagens sobre os eventos de comemoração ao aniversário e outras curiosidades da UFPA.</p>

            <h4>#EuNaUFPA</h4>

            <p>Um dos diferenciais de um webdocumentário é a possibilidade de interação com o público. Por isso, a Rádio Web preparou um espaço destinado à participação colaborativa dos usuários. A #EuNaUFPA funciona como uma galeria e, inicialmente, é composta por vídeos e fotos de estudantes ligados ao projeto, mas a ideia é que qualquer pessoa possa compartilhar conteúdos, como espaços favoritos ou mensagens a partir das redes sociais, utilizando a hashtag #EuNaUFPA. Desta forma, será possível colher novos depoimentos e conhecer diferentes lugares da instituição, principalmente, dos campi do interior do Estado.</p>

            <h4>Produção e aprendizado</h4>

            <p>Conectada aos novos tempos em que as mídias convergem entre si e diante de um público cada vez mais exigente, a Rádio Web UFPA se reinventa, agregando novas formas de comunicação a partir do ambiente web.</p>

            <p>O trabalho de produção do Webdocumentário sobre os 60 anos da UFPA exigiu especial empenho e dedicação da equipe, acostumada a trabalhar até então somente com gravação e edição de produtos em áudio. “Foi uma experiência diferente e desafiadora em diversos sentidos, desde a abordagem do tema, o uso das diversas linguagens midiáticas e, principalmente, o olhar sobre o público. O resultado é um grande aprendizado e também um trabalho que nos dá muito orgulho”, declara o jornalista Fabrício Queiroz.</p>

            <p>Para a bolsista de jornalismo Graziela Ferreira, participar da equipe que produziu o Webdoc foi uma experiência agregadora: “pude colocar em prática todos os ensinamentos que foram repassados durante a graduação e ainda tive a oportunidade de conhecer um pouco das histórias da UFPA, que para mim se tornou minha segunda casa”, completa.</p>

            <p>Garantir a acessibilidade do webdocumentário tornou-se também um compromisso e um aprendizado importante para toda a equipe, além de um diferencial ao produto. Para a professora Rosane Steinbrenner, diretora da Faculdade de Comunicação e coordenadora da Rádio Web, “garantir uma comunicação mais inclusiva e acessível é uma missão de toda instituição pública e um desafio de todos nós, ainda mais em espaços de formação de profissionais da comunicação, como a FACOM”.</p>

            <p>Desde 2016, a Rádio vem realizando experiências em produções radiofônicas diferenciadas para ocasiões especiais, como os 400 anos de Belém, os 40 anos da Faculdade de Comunicação da UFPA e o processo de eleição para a reitoria da Universidade. O webdocumentário é a primeira experiência multimidiática e oferece ao público a possibilidade de imergir no passado da Universidade, ao mesmo tempo que conhece o presente e reflete sobre o futuro.</p>

           <h4>Sobre a Rádio Web UFPA:</h4>

            <p>A Rádio Web UFPA entrou no ar oficialmente em maio de 2009, com a proposta de ser um canal de divulgação das atividades científicas e acadêmicas realizadas pela Universidade Federal do Pará. Nesses oito anos no ar, a Rádio mantém uma programação regular voltada à divulgação científica com produções radiofônicas que abordam as mais diversas áreas de atuação da UFPA. O projeto está vinculado à Faculdade de Comunicação e ao Instituto de Letras e Comunicação, sendo executado com o apoio da Pró-reitoria de Administração (PROAD) e Fundação de Amparo e Desenvolvimento da Pesquisa (Fadesp).</p>

