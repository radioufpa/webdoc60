              <div>

                <h3>Anos 60</h3>

                <p>A década de 60 foi marcada pela repressão, censura e violência do período de ditadura militar em que vivia o Brasil. Os anos 60 ficaram conhecidos como anos de mudança, que promoveram transformações sociais no país. Lutava-se contra o regime militar implementado em 1964, contra a reforma educacional, o que mais tarde provocou o fechamento do congresso nacional.</p>
                <p>A Universidade encontrava-se estagnada naquele período e foi realizado um processo de revitalização da instituição pelo então reitor dr. José Rodrigues da Silveira Neto, promovendo ampla restruturação acadêmica e administrativa por meio de uma reforma estatutária que definiu a política educacional, suas normas, diretrizes de pesquisa e projetos de extensão. Um dos marcos desse período é a instalação do campus do Guamá, localizado em uma área de aproximadamente 200 hectares, na confluência do Igarapé Tucunduba com o Rio Guamá.</p>

              </div>
            </li>

