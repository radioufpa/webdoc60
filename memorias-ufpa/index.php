                <h3>Anos 50</h3>
                </span>

                <p>Também conhecida como Anos Dourados, a década de 50 foi marcada por uma forte revolução industrial e econômica que implicou em grandes transformações para a sociedade brasileira. O Brasil era governado por Juscelino Kubitscheck e estava em um período de modernização com fomento à industrialização nacional.</p>
                <p>O país também vivia um momento de abertura econômica ao capital externo para investimento. Em 02 de julho de 1957, após cinco anos de tramitação legislativa, foi sancionada por JK, a lei de nº 3.191, criando a Universidade do Pará, que passava a congregar sete faculdades existentes. Dezoito meses depois, em 31 de janeiro de 1959, a UFPA foi festivamente instalada em sessão solene realizada no Theatro da Paz.</p>

