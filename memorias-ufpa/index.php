                </figure>

              <div>

                <h3>Anos 60</h3>

                <p>A década de 60 foi marcada pela repressão, censura e violência do período de ditadura militar em que vivia o Brasil. Os anos 60 ficaram conhecidos como anos de mudança, que promoveram transformações sociais no país. Lutava-se contra o regime militar implementado em 1964, contra a reforma educacional, o que mais tarde provocou o fechamento do congresso nacional.</p>
                <p>A Universidade encontrava-se estagnada naquele período e foi realizado um processo de revitalização da instituição pelo então reitor dr. José Rodrigues da Silveira Neto, promovendo ampla restruturação acadêmica e administrativa por meio de uma reforma estatutária que definiu a política educacional, suas normas, diretrizes de pesquisa e projetos de extensão. Um dos marcos desse período é a instalação do campus do Guamá, localizado em uma área de aproximadamente 200 hectares, na confluência do Igarapé Tucunduba com o Rio Guamá.</p>

              </div>
            </li>

            <li>
                
                <figure class="left-photo">
                   <a data-fancybox="linha-do-tempo" data-caption="Anos 70 - Inauguração do Núcleo de Ciências Geofísicas e Geológicas, em 1978." href="../_media/images/memorias/anos-70-ufpa-inauguracao-geofisica-geologia.jpg"><img src="../_media/images/memorias/anos-70-ufpa-inauguracao-geofisica-geologia.jpg"></a>
                </figure>

              <div>

                <h3>Anos 70</h3>

                <p>A ditadura militar chega ao auge de sua popularidade no início da década de 70. O regime intensificava a censura aos meios de comunicação do país, torturava e exilava os indivíduos considerados subversivos à ditadura. As mobilizações contrárias ao regime também ganharam força com episódios marcantes, como a Guerrilha do Araguaia, no Pará.</p>
                <p>Nesse período, a Universidade Federal do Pará inicia suas atividades de pesquisa e formação de recursos humanos qualificados para a região. Em fevereiro de 1973 surge o Curso de Pós-Graduação em Geofísica (CPGf), o primeiro mestrado da instituição. No mesmo ano, surge também o Núcleo de Altos Estudos Amazônicos (NAEA), referência na discussão sobre desenvolvimento regional e a sustentabilidade na região.</p>

              </div>
            </li>

            <li>
                
                <figure class="right-photo">
                   <a data-fancybox="linha-do-tempo" data-caption="Anos 80 - Campus Universitário de Soure, criado em 1987." href="../_media/images/memorias/anos-80-ufpa-campus-soure.jpg"><img src="../_media/images/memorias/anos-80-ufpa-campus-soure.jpg"></a>
                </figure>

              <div>
                
                <h3>Anos 80</h3>

                <p>Com a perda do apoio popular na década de 80, o regime militar estava chegando ao fim. Este período foi marcado pela redemocratização do Brasil, impulsionada pela crise que vivia a ditadura devido à intensificação das manifestações populares que reivindicavam eleições diretas.</p>
                <p>No campo da educação, a Universidade Federal do Pará implanta o projeto de interiorização, que visava levar a formação superior para polos distribuídos pelo interior do estado, onde inicialmente são atendidas as demandas por cursos de licenciatura. Nesse contexto surgiram os campi de Abaetetuba, Altamira, Cametá, Castanhal, Bragança, Marabá, Santarém e Soure.</p>

              </div>
            </li>

            <li>

                <figure class="left-photo">
                   <a data-fancybox="linha-do-tempo" data-caption="Anos 90 - Criação da Rede de Capacitação em Educação a Distância. Foto: Alexandre de Moraes." href="../_media/images/memorias/anos-90-ufpa-educacao-a-distancia-ead.jpg"><img src="../_media/images/memorias/anos-90-ufpa-educacao-a-distancia-ead.jpg"></a>
                </figure>

              <div>
                
                <h3>Anos 90</h3>

                <p>A década de 90 foi marcada por um período de instabilidade e mudanças na política e econômia do Brasil. É também um momento de grande desenvolvimento tecnológico em todo o mundo, com aperfeiçoamento e popularização de tecnologias que logo se tornaram ferramentas que facilitariam o acesso ao ensino.</p>
                <p>Diante do quadro educacional vivido, em 1996 foi criado o Programa de Educação a Distância como opção de democratização do acesso ao saber. A EaD surgiu como uma prática pedagógica voltada aos jovens e adultos excluídos ou impossibilitados, por questões geográficas, econômicas ou outras, de frequentar o sistema formal de ensino, e, por isso, logo, se tornou uma alternativa para a ampliação do ensino superior na Amazônia.</p>

              </div>
            </li>

            <li>

                <figure class="right-photo">
                   <a data-fancybox="linha-do-tempo" data-caption="Anos 2000 - Centro de Eventos Benedito Nunes, inaugurado em 2009. Foto: Alexandre de Moraes." href="../_media/images/memorias/anos-2000-ufpa-inauguracao-centro-eventos-benedito-nunes.jpg"><img src="../_media/images/memorias/anos-2000-ufpa-inauguracao-centro-eventos-benedito-nunes.jpg"></a>
                </figure>
              <div>

                <h3>Anos 2000</h3>

                <p>A década de 2000 ficou marcada pela redução das desigualdades sociais no Brasil através do reforço de políticas sociais e de redistribuição de renda para o combate a pobreza. A educação foi uma das áreas beneficiadas nesse período. No ano de 2007, o decreto nº 6.096 criou o Programa de Apoio a Planos de Reestruturação e Expansão das Universidades Federais (REUNI), que previa ações com o objetivo de retomar o crescimento do ensino superior público.</p>
                <p>Entre os impactos dessa política estão a estruturação física e acadêmica das universidades já existentes, além da criação de novas instituições regionalizadas, como é o caso da Universidade Federal do Oeste do Pará (UFOPA), oriunda do campus da UFPA em Santarém.</p>

              </div>
            </li>

            <li>

                <figure class="left-photo">
                   <a data-fancybox="linha-do-tempo" data-caption="Anos 2010 - Alunos da UFPA conversando na beira do rio. Foto: Alexandre de Moraes." href="../_media/images/memorias/anos-2010-ufpa-alunos-beira-rio.jpg"><img src="../_media/images/memorias/anos-2010-ufpa-alunos-beira-rio.jpg"></a>
                </figure>
              <div>

                <h3>Anos 2010</h3>

                <p>Educação, ciência, tecnologia e inovação foram áreas que receberam um incremento de investimentos no início desta década. O avanço do ensino superior na Amazônia foi acompanhado também por medidas de incentivo à diversidade e à inclusão. A UFPA foi uma das instituições pioneiras na adoção de políticas afirmativas em seus processos seletivos, tendo as primeiras medidas iniciado em 2008.</p>
                <p>Já a partir de 2012, com a sanção lei nº 12.711, as políticas afirmativas passaram a ser adotadas em todas as instituições de ensino superior público. Na Universidade Federal do Pará, desde 2016, foram adotados três tipos de cotas: cota escola, a cota renda e cota pretos, pardos e índios. Além disso, ocorre a criação de vagas extras para pessoas com deficiência e processos seletivos especiais para estudantes de origem indígena ou quilombola.</p>

              </div>
            </li>

        </ul>

